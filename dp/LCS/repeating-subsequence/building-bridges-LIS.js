const maxBridgesCount = (north, south) => {
  const coordinates = []
  for (let i = 0; i < north.length; i++) {
    const pair = [north[i], south[i]]
    coordinates.push(pair)
  }
  // sort the pair according to north values
  coordinates.sort((a, b) => a[0] - b[0])
  // extract the values for southern array
  const input = coordinates.map((a) => a[1])
  const result = lengthOfLIS(input, 0, -1)
  return result
}

const lengthOfLISRec = (input, current, previous) => {
  if (current >= input.length) return 0 // we have reached the end
  // skip the current
  let max = lengthOfLISRec(input, current + 1, previous)
  if (input[current] > (input[previous] || -Infinity)) {
    const count = lengthOfLISRec(input, current + 1, current)
    max = Math.max(max, 1 + count)
  }
  return max
}

const lengthOfLIS = (input) => {
  const dp = new Array(input.length).fill(1)
  let longest = 1
  for (let i = 1; i < input.length; i++) {
    for (let j = 0; j < i; j++) {
      if (input[j] < input[i]) {
        dp[i] = Math.max(dp[i], dp[j] + 1)
        longest = Math.max(longest, dp[i])
      }
    }
  }
  return longest
}

