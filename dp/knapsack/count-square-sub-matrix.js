function countSquares (matrix) {
  const rows = matrix.length
  const columns = matrix[0].length
  let count = 0
  const lookUp = [...Array(matrix.length)].map(() => Array(matrix[0].length).fill(-1))
  for (let r = 0; r < rows; r++) {
    for (let c = 0; c < columns; c++) {
      // if the value of current element is 1 then count the number of square matrix recursively
      if (matrix[r][c] === 1 && lookUp[r][c] === -1) {
        count += countSquaresRecursiveMemo(matrix, rows, columns, r, c, lookUp)
      } else {
        count += matrix[r][c]
      }
    }
  }

  return count
}

const countSquaresRecursive = (matrix, rows, columns, r, c) => {
  // base case
  if (r >= rows || c >= columns || matrix[r][c] === 0) {
    return 0
  }
  // for every element check the right, bottom, bottom-right
  const right = countSquaresRecursive(matrix, rows, columns, r, c + 1)
  const bottom = countSquaresRecursive(matrix, rows, columns, r + 1, c)
  const bottomRight = countSquaresRecursive(matrix, rows, columns, r + 1, c + 1)
  // 1 because 1 is in itself a matrix.
  // Min because we only want to count where there are all 1 and an element might be part of a larger matrix that is not part of this
  // Build up a matrix table where for ever element you fill the number of square it is contributing to for visualization
  // https://www.youtube.com/watch?v=6X7Ha2PrDmM

  return 1 + Math.min(right, bottom, bottomRight)
}

const countSquaresRecursiveMemo = (matrix, rows, columns, r, c, lookUp) => {
  // Base case
  if (r >= rows || c >= columns || matrix[r][c] === 0) return 0
  if (lookUp[r][c] === -1) {
    const right = countSquaresRecursiveMemo(matrix, rows, columns, r, c + 1, lookUp)
    const bottom = countSquaresRecursiveMemo(matrix, rows, columns, r + 1, c, lookUp)
    const bottomRight = countSquaresRecursiveMemo(matrix, rows, columns, r + 1, c + 1, lookUp)
    lookUp[r][c] = 1 + Math.min(right, bottom, bottomRight)
  }
  return lookUp[r][c]
}

const countSquaresBottomUp = (matrix) => {
  const rows = matrix.length
  const columns = matrix[0].length
  const dp = [...Array(rows + 1)].map(() => Array(columns + 1).fill(0))

  // Build the count matrix where ever element position is filled with the number of square it is part of
  let count = 0
  let max = 0
  for (let r = rows - 1; r >= 0; r--) {
    for (let c = columns - 1; c >= 0; c--) {
      // this is the case for including the element , we do not count the element that we are not including
      if (parseInt(matrix[r][c]) === 1) {
        // matrix[r][c] = 1 + Min(left, up, diagonal)
        // instead of index out of bound check we could have build at matrix of column +1 and rows +1 all filled with zeros
        // const right = c + 1 < columns ? dp[r][c + 1] : 0
        // const bottom = r + 1 < rows ? dp[r + 1][c] : 0
        // const diag = (r + 1 < rows && c + 1 < columns ) ? dp[r +1][c +1] : 0
        const right = parseInt(dp[r][c + 1])
        const bottom = parseInt(dp[r + 1][c])
        const diag = parseInt(dp[r + 1][c + 1])
        dp[r][c] = 1 + Math.min(right, bottom, diag)
        count += dp[r][c]
        if (max < dp[r][c]) {
          max = dp[r][c]
        }
      }
    }
  }
  console.info(`Max is  ${max}`)
  console.info(`Max  Area is  ${max * max}`)
  print2DMatrix(dp)
  return count
}
